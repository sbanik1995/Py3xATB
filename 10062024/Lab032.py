# Ternary Operator
sourav_marks = 90
amit_marks = 97

# if x>y -> do something -> print("sourav")
# if y>x -> do something else -> print("amit")

print("Sourav") if sourav_marks>amit_marks else print("Amit is winner")

if sourav_marks>amit_marks:
    print("Sourav is the winner")
else:
    print("Amit is the winner")
