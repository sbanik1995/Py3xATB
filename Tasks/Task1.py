# 1. Explain the Difference between = and == Operators in Python
# =Operator is used to assign a value to a variable. It is an assignment operator. Ex:- a = 9 where value 9 is assigned to the variable a
# ==Operator is used to compare the values of two variables. This is an equality operator. Ex:- a == 9 where it checks if the value 9 is equals to a or not.


# 2. What does the ** operator do in Python, and how is it used?
# The ** operator is an exponentiation Operator. It is used to increase the power of a number 2 or 3 times. Ex:- 3**3 which results to 27


