# Sum of two numbers using Lambda function

o_f = lambda a,b : a+b
print(o_f(10,20))

# Subraction of two numbers using Lambda function

o_f = lambda a,b : a-b
print(o_f(10, 20))

# Multiplication of two numbers using Lambda function

o_f = lambda a,b : a*b
print(o_f(10, 20))

# Division of two numbers using Lambda function

o_f = lambda a,b : a/b
print(o_f(20, 20))

