# For Loop
# For Counter in range

for counter in range(0, 100):
    print(counter)

for counter in range(0, 101):
    print(counter)

for counter in range(0, 101, 2):
    print(counter)

for counter in range(1, 101, 2):
    print(counter)


for counter in range(1, 101):
    print(counter)
    if counter == 5:
        break

