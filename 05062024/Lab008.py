# Dynamically typed
# Dynamic type - Types of data that oython supports
age = 65

# Variable Name = Variable Value
# identifier = literal
# Types which are supported in python
# Integers(int) - positive or negative whole numbers ( Ex- 1,2,100,-10,-11 )

# Floating(float) point numbers (Ex- 3.14, 5.333, 18.00, 0.000678, -0.4, -1.6 )
pi = 3.14

# String(str) - Bunch of characters within " " ( Ex- " Sourav ", " Hello World " , " 12345 " )
name = " Sourav "

# Boolean(bool) - True or False
isMale = True

print(type(age))
print(type(name))
print(type(isMale))
print(type(pi))

# python - Complex Numer - i iota
complex_number = 2+3j
# Real part = 2
# Imaginary part = 3
print(type(complex_number.real))
print(type(complex_number.imag))


